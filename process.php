			</h2>
			<ul>
				<li>2 Pieces of fabroc 13.5" x 14" outside</li>
				<li>2 pieces of fabric 13.5" x 14" long</li>
				<li>1 piece 4" x 44" long</li>
			</ul>
			<h2>
				The Straps
			</h2>
			<ul>
				<li>Cut a piece of fabric 4"x44" long. Press fabric in half lengthwise</li>
				<li>Open fabric up and fold the outside into the center.</li>
				<li>Fold fabric in half, and then press.</li>
				<li>TOp-stitch down each side of strap close to the edge. Cut fabric in half so you have two straps about 22" long, each.</li>
			</ul>
			<h2>
				The Bag
			</h2>
			<ul>
				<li>Cur four pireces of fabric to 13.5" x 14".</li>
				<li>Sew lining fabric right sides together on three sides and also outer fabric right sides together on three sides. For this bag, the bottom is 13.5" wide and the sides 14" long. The bag can be sized to fit whatever you want.</li>
				<li>Pinch bottom corner together and stitch across 1.5" from the corner.</li>
				<li>Repeat the step above until all four corners have been sewn.</li>
				<li>Place lining fabric inside the outside fabric right sides together.</li>
				<li>Place straps in between right sides of fabric on each side about two to three inches from the side edge.</li>
				<li>Pin straps and top edge of bag well. Stitch around the top of the bag .5" from the edge. Be sure to leave around 2-3" of an opening for turning the bag. This can easily be done by leaving the opening to the side.</li>
				<li>Turn the bag inside out.</li>
				<li>Push the lining inside of the bag and top stitch around the top of the bag. This will close up the opening used for turning.</li>
			</ul>
